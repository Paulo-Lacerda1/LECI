#Como calcular a área do triangulo#

print("Para calcular a área do triangulo insira as seguintes medidas:") 

comprimento = float (input("Qual é o comprimento da base?"))
  
altura = float (input ('Qual é a altura do triangulo '))

área = comprimento*altura /2

print('A área do triangulo é:{}'.format(área))


#Como calcular o perimetro do triangulo?#


print("Para calcular o perimetro do triangulo insira as seguintes medidas:")

lado1 = float( input("Qual é o comprimento do lado 1?"))
lado2 = float( input("Qual é o comprimento do lado 2?"))
lado3 = float( input("Qual é o comprimento do lado 3?"))

perimetro= lado1 + lado2 + lado3 

print('O perimetro do trinagulo é:{}'.format(perimetro))